#include <stdio.h>
#include <string.h>

void	*ft_memchr(const void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*dest;

	i = 0;
	dest = (unsigned char *)b;
	while (i < len)
	{
		if (dest[i] == (unsigned char) c)
			return (&dest[i]);
		i++;
	}
	return (0);
}
